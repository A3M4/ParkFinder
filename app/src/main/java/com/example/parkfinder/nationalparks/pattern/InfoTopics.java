package com.example.parkfinder.nationalparks.pattern;

public class InfoTopics {
    private String id;
    private String name;

    public InfoTopics() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

